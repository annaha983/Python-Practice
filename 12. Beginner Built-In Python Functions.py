#Built-in Python Functions

#abs - Strips the negative sign.
print(abs(-111))

#bool is the truth checker. If it is a positive number then it yeilds true. 
print(bool(100))
print(bool(0))

#dir highlights all the methods you can use with a particular object
print(dir('Hello'))

#help with .upper tells how the method works and how you can use it. 
print(help('Hello'.upper))

sent = "print('hi')"
eval(sent)

print("Hello " + str(100))

print(int("123") + 456)
#This prints a number integer and then adds to it. 

print(float("123.456") + 0.36)
